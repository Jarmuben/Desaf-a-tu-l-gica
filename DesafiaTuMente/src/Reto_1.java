
public class Reto_1 {

	public static void main(String[] args) {
		// Declaro la variable serie inicializándola a valor 10

		int serie = 10;

		// Ahora pongo While con lo que vamos a trabajar y entre paréntesis pongo la
		// variable o elemento que quiero evaluar.
		// Quiero ver si la variable primera es mayor o igual a 1.

		while (serie >= 1) {

			// En caso de que cumpla la condición y "SERIE" sea mayor o igual a 1 quiero que
			// me imprima el valor de "Serie".

			System.out.println(serie);

			serie--;
		}

	}
}
